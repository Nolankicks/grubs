namespace Grubs.Terrain;

/// <summary>
/// Defines the mode in which to edit terrain.
/// </summary>
public enum TerrainModifyMode
{
	/// <summary>
	/// Adds to the terrain.
	/// </summary>
	Add,
	/// <summary>
	/// Removes from the terrain.
	/// </summary>
	Remove,
	/// <summary>
	/// Toggles points in the terrain.
	/// </summary>
	Toggle
}
